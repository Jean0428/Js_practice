//Practicing Comparison Operators, Relational and Equality Operators
// Declaration of favorite number
let firstFavNum = 63;
let secondFavNum = 26;

//Utilizing the Relational Operators
let GT = firstFavNum > secondFavNum; //Greater than
let LT = firstFavNum < secondFavNum; //Less Than
let GTorET = firstFavNum >= secondFavNum; //Greater Than or equal to
let LTorET = firstFavNum <= secondFavNum; //Less Than or Equal To

//Utilizing The Equality Operators
let ST = firstFavNum === secondFavNum; //Strict Equality
let LS = firstFavNum == secondFavNum; //Loose Equality
let SI = firstFavNum !== secondFavNum; //Strict Inequality
let LI = firstFavNum != secondFavNum; //Loose Inequality

//To log all the variables
console.log(GT);
console.log(LT);
console.log(GTorET);
console.log(LTorET);
console.log(ST);
console.log(LS);
console.log(SI);
console.log(LI);